                <p>Dr. Shung is a life fellow of IEEE, and a fellow of American Institute of Ultrasound in Medicine. He is a founding fellow of American Institute of Medical and Biological Engineering. He received the IEEE Engineering in Medicine and Biology Society Early Career Award in 1985 and was the coauthor of a paper that received the best paper award for IEEE Transactions on Ultrasonics, Ferroelectrics and Frequency Control (UFFC) in 2000. He was elected an outstanding alumnus of Cheng-Kung University in Taiwan in 2001. He was selected as the distinguished lecturer for the IEEE UFFC society for 2002-2003. He received the Holmes Pioneer Award in Basic Science from American Institute of Ultrasound in Medicine in 2010 and the academic career achievement award from the IEEE Engineering in Medicine and Biology Society in 2011. He is the recipient of IEEE Biomedical Engineering Award in 2016. Dr. Shung has published more than 500 papers and book chapters. He is an associate editor of IEEE Transactions on UFFC, IEEE Transactions on Biomedical Engineering and Medical Physics. He is an editorial board member of Scientific Reports. Dr. Shung's research interest is in ultrasonic transducers, high frequency ultrasonic imaging and ultrasound microbeam. 
